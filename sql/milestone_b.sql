ADD COLUMN IF NOT EXISTS speed INTEGER DEFAULT 10,
ADD COLUMN IF NOT EXISTS hp INTEGER DEFAULT 100,
ADD COLUMN IF NOT EXISTS max_hp INTEGER DEFAULT 100,
ADD COLUMN IF NOT EXISTS stamina INTEGER DEFAULT 100,
ADD COLUMN IF NOT EXISTS max_stamina INTEGER DEFAULT 100,
ADD COLUMN IF NOT EXISTS fruit TEXT,
